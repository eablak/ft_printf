#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	len = 0;
	if (s == NULL)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (*s)
	{
		len += ft_putchr(*s);
		s++;
	}
	return (len);
}
